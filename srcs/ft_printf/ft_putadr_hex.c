#include "pipex.h"

int	ft_putadr_hexa(unsigned long adr, int *count)
{
	char	*base;
	char	tab[16];
	int		i;

	if (!adr)
	{
		if (write(2, "(nil)", 5) == -1)
			return (-1);
		*count += 5;
		return (1);
	}
	base = "0123456789abcdef";
	i = 15;
	while (adr >= 16 && i > 0)
	{
		tab[i] = base[adr % 16];
		i--;
		adr = adr / 16;
	}
	tab[i] = base[adr % 16];
	if (write(2, "0x", 2) == -1 || write(2, &tab[i], -(i - 16)) == -1)
		return (-1);
	*count += 2 + (-(i - 16));
	return (1);
}
